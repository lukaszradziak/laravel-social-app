    /**
     * Handle the Post "created" event.
     *
     * @param  \App\Models\Post  $post
     * @return void
     */
    public function created(Post $post)
    {
        $hashtags = [];
        foreach ($post->parseHashtags() as $name) {
            $hashtag = Hashtag::firstOrCreate(['name' => $name]);
            $hashtags[] = $hashtag->id;
        }
        if (count($hashtags)) {
            $post->hashtags()->sync($hashtags);
        }
    }

